
public class juguetesAlmacen {
    int pelota;
    int muñeca;
    int barco;
    int carrito;
    int bom;
    int crayones;
    int lego;

    public juguetesAlmacen(){
        
    }
    public juguetesAlmacen(int pelota, int muñeca, int barco, int carrito, int bom, int crayones, int lego) {
        this.pelota = pelota;
        this.muñeca = muñeca;
        this.barco = barco;
        this.carrito = carrito;
        this.bom = bom;
        this.crayones = crayones;
        this.lego = lego;
    }

    public int getPelota() {
        return pelota;
    }

    public void setPelota(int pelota) {
        this.pelota = pelota;
    }

    public int getMuñeca() {
        return muñeca;
    }

    public void setMuñeca(int muñeca) {
        this.muñeca = muñeca;
    }

    public int getBarco() {
        return barco;
    }

    public void setBarco(int barco) {
        this.barco = barco;
    }

    public int getCarrito() {
        return carrito;
    }

    public void setCarrito(int carrito) {
        this.carrito = carrito;
    }

    public int getBom() {
        return bom;
    }

    public void setBom(int bom) {
        this.bom = bom;
    }

    public int getCrayones() {
        return crayones;
    }

    public void setCrayones(int crayones) {
        this.crayones = crayones;
    }

    public int getLego() {
        return lego;
    }

    public void setLego(int lego) {
        this.lego = lego;
    }
    
}
