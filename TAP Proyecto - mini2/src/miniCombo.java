
public class miniCombo {
    String combo;
    String juguete;
    
    public miniCombo(String combo, String juguete){
        
        this.combo = combo;
        this.juguete = juguete;
    }

    public miniCombo() {
    }

    public String getCombo() {
        return combo;
    }

    public void setCombo(String combo) {
        this.combo = combo;
    }

    public String getJuguete() {
        return juguete;
    }

    public void setJuguete(String juguete) {
        this.juguete = juguete;
    }
    
}
