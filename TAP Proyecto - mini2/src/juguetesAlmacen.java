
public class juguetesAlmacen {
    String pelota;
    String muñeca;
    String barco;
    String carrito;
    String bom;
    String crayones;
    String lego;

    public juguetesAlmacen(){
        
    }
    public juguetesAlmacen(String pelota, String muñeca, String barco, String carrito, String bom, String crayones, String lego) {
        this.pelota = pelota;
        this.muñeca = muñeca;
        this.barco = barco;
        this.carrito = carrito;
        this.bom = bom;
        this.crayones = crayones;
        this.lego = lego;
    }

    public String getPelota() {
        return pelota;
    }

    public void setPelota(String pelota) {
        this.pelota = pelota;
    }

    public String getMuñeca() {
        return muñeca;
    }

    public void setMuñeca(String muñeca) {
        this.muñeca = muñeca;
    }

    public String getBarco() {
        return barco;
    }

    public void setBarco(String barco) {
        this.barco = barco;
    }

    public String getCarrito() {
        return carrito;
    }

    public void setCarrito(String carrito) {
        this.carrito = carrito;
    }

    public String getBom() {
        return bom;
    }

    public void setBom(String bom) {
        this.bom = bom;
    }

    public String getCrayones() {
        return crayones;
    }

    public void setCrayones(String crayones) {
        this.crayones = crayones;
    }

    public String getLego() {
        return lego;
    }

    public void setLego(String lego) {
        this.lego = lego;
    }
    
}
